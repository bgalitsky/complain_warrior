}

status_all() {
  log "=== STATUS ==="

  if systemctl is-active --quiet nginx; then
    log "nginx: active"
  else
    log "nginx: inactive"
  fi

  for svc in twilio oauth streamlit entry_frontend small_claims charge_back cw_regulatory ngrok; do
    pidfile="$PID_DIR/$svc.pid"
    pid="$(read_pid "$pidfile")"
    if [[ -n "$pid" ]] && is_running_pid "$pid"; then
      log "$svc: RUNNING (pid $pid)"
    else
      log "$svc: NOT running"
    fi
  done

  log "Listening ports (best-effort):"
  ss -ltnp 2>/dev/null | grep -E ':80 |:5000 |:8509 |:8510 |:8512 |:8513 |:8514 |:8515 ' || true
  log "Nginx route config: $NGINX_ROUTE_CONFIG"
  if [[ -f "$NGINX_ROUTE_CONFIG" ]]; then
    log "nginx route file exists"
  else
    log "nginx route file is missing"
  fi
  log "PUBLIC_BASE=$PUBLIC_BASE"
  log "CW_CUSTOMER_APP_URL=${CW_CUSTOMER_APP_URL:-${PUBLIC_BASE}/complaint_warrior}"
  log "CW_COMPANY_APP_URL=${CW_COMPANY_APP_URL:-${PUBLIC_BASE}/complaint_warrior}"
  log "CW_SMALL_CLAIMS_APP_URL=${CW_SMALL_CLAIMS_APP_URL:-${PUBLIC_BASE}/small_claim_court_warrior}"
  log "CW_CHARGE_BACK_APP_URL=${CW_CHARGE_BACK_APP_URL:-${PUBLIC_BASE}/charge_back_initiator}"
  log "CW_REGULATORY_APP_URL=${CW_REGULATORY_APP_URL:-${PUBLIC_BASE}/cw_regulatory}"
  log "CW_FB_POSTER_DOWNLOAD_URL=${CW_FB_POSTER_DOWNLOAD_URL:-${PUBLIC_BASE}/downloads/fb-poster/}"
}

start_stack() {
  log "Starting stack in $APP_DIR"
  start_nginx
  start_twilio
  start_oauth
  start_complaint_warrior
  start_small_claims
  start_charge_back
  start_cw_regulatory
  start_ngrok
  start_entry_frontend
  status_all
}

case "${1:-start}" in
  stop)
    stop_all
    ;;
  restart)
    stop_all
    start_stack
    ;;
  start)
    stop_all
    start_stack
    ;;
  status)
    status_all
    ;;
  *)
    echo "Usage: $0 {start|stop|restart|status}"
    exit 1
    ;;
esac
