def fallback_rule_based_drafts(record: ComplaintRecord, registry: List[Dict[str, str]]) -> List[OutreachDraft]:
    """Works without OpenAI. Uses registry and a standard complaint letter."""
    company = record.company_name or "the company"
    selected_keys = ["bbb", "state_ag", "ftc", "credit_card", "company_exec"]
    text = record.display_text()
    drafts: List[OutreachDraft] = []
    for org in registry:
        if org["key"] not in selected_keys:
            continue
        org_name = org["name"]
        subject = f"Consumer complaint regarding {company}"
        body = f"""Dear {org_name},

I am submitting this consumer complaint regarding {company}.

Summary of complaint:
{text}

Requested resolution:
I request investigation, mediation, or appropriate referral, and I request that {company} refund any undisclosed or improperly imposed charges and provide a written explanation of the transaction.

Basis for complaint:
The issue involves an alleged undisclosed mandatory charge, refusal to honor prepaid or represented terms, and possible unfair or deceptive business practice. I am preserving receipts, reservation confirmations, screenshots, and related correspondence as supporting evidence.

Please confirm receipt of this complaint and advise if additional documentation is required.

Sincerely,
{record.user_name or '[Your name]'}
{record.user_email or ''}
""".strip()
        drafts.append(
            OutreachDraft(
                include=True,
                org_key=org["key"],
                org_name=org_name,
                category=org.get("category", ""),
                recommended_channel=org.get("recommended_channel", ""),
                email=org.get("email", ""),
                web_url=org.get("web_url", ""),
                why_this_org=org.get("notes", ""),
                subject=subject,
                body=body,
                caution="Email is empty unless verified. Use web form/copy-paste where applicable.",
            )
        )
    return drafts


def extract_json_object(text: str) -> Dict[str, Any]:
    text = text.strip()
    try:
        return json.loads(text)
    except Exception:
        pass
    m = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if not m:
        raise ValueError("Model did not return JSON.")
    return json.loads(m.group(0))


def generate_outreach_drafts_with_openai(
    record: ComplaintRecord,
    registry: List[Dict[str, str]],
    jurisdiction_hint: str,
    extra_instruction: str,
) -> List[OutreachDraft]:
    if OpenAI is None:
        raise RuntimeError("OpenAI package is not installed. Run: pip install openai")
    api_key = get_openai_api_key()
    if not api_key:
        raise RuntimeError("OpenAI API key not found. Put it in ./config.ini as [OpenAI] api_key = ...")

    client = OpenAI(api_key=api_key)
    complaint_payload = {
        "complaint_id": record.complaint_id,
        "subject": record.subject,
        "company_name": record.company_name,
        "user_name": record.user_name,
        "user_email": record.user_email,
        "complaint_text": record.display_text(),
        "jurisdiction_hint": jurisdiction_hint,
    }
    system = """You are a careful U.S. consumer-complaint routing and drafting assistant.
Return STRICT JSON only.
Your task: recommend organizations to contact and draft individualized complaint letters.
Rules:
- Do not provide legal advice as an attorney.
- Do not invent recipient email addresses.
- If an organization normally uses a web form, set email to empty and recommended_channel to web_form.
- Only use emails already present in the provided organization registry or explicitly present in the complaint/user instruction.
- Use cautious language: suspected, alleged, appears, I believe. Do not conclusively accuse anyone of a crime.
- Avoid defamation and exaggeration. Keep facts specific and verifiable.
- Each letter should request a concrete remedy, confirmation of receipt, and instructions for evidence submission.
- For BBB, FTC, state AG, FDACS, IC3, etc., drafts may be used as web-form text.
JSON shape:
{
  "drafts": [
    {
      "include": true,
      "org_key": "registry key or extra_org",
      "org_name": "name",
      "category": "category",
      "recommended_channel": "email_if_verified|web_form|secure_message_or_mail|manual",
      "email": "verified email or empty string",
      "web_url": "url or empty string",
      "why_this_org": "1-2 sentence reason",
      "subject": "email/form subject",
      "body": "full complaint letter",
      "caution": "any review warning"
    }
  ]
}
"""
    user = {
        "complaint": complaint_payload,
        "organization_registry": registry,
        "extra_instruction": extra_instruction,
    }
    resp = client.chat.completions.create(
        model=OPENAI_MODEL,
        messages=[
            {"role": "system", "content": system},
            {"role": "user", "content": json.dumps(user, ensure_ascii=False, indent=2)},
        ],
        temperature=0.2,
        response_format={"type": "json_object"},
    )
    raw = resp.choices[0].message.content or "{}"
    parsed = extract_json_object(raw)
    drafts: List[OutreachDraft] = []

    # Preserve known email/web metadata from registry. GPT can explain and draft, but
    # verified recipient data should come from registry/user review.
    by_key = {o["key"]: o for o in registry}
    for item in parsed.get("drafts", []):
        if not isinstance(item, dict):
            continue
        key = clean_text(item.get("org_key"))
        reg = by_key.get(key, {})
        email = clean_text(reg.get("email")) or clean_text(item.get("email"))
        # If GPT supplied an email that was not in registry, keep it but force review.
        caution = clean_text(item.get("caution"))
        if email and not clean_text(reg.get("email")):
            caution = (caution + "\n" if caution else "") + "GPT/user-supplied email: verify before sending."
        drafts.append(
            OutreachDraft(
                include=bool(item.get("include", True)),
                org_key=key or "extra_org",
                org_name=clean_text(item.get("org_name")) or clean_text(reg.get("name")) or "Organization",
                category=clean_text(item.get("category")) or clean_text(reg.get("category")),
                recommended_channel=clean_text(item.get("recommended_channel")) or clean_text(reg.get("recommended_channel")),
                email=email,
                web_url=clean_text(reg.get("web_url")) or clean_text(item.get("web_url")),
                why_this_org=clean_text(item.get("why_this_org")) or clean_text(reg.get("notes")),
                subject=clean_text(item.get("subject")) or f"Consumer complaint regarding {record.company_name or 'merchant'}",
                body=clean_text(item.get("body")),
                caution=caution,
            )
        )
    if not drafts:
        raise ValueError("OpenAI returned no drafts.")
    return drafts


