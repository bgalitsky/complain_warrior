"""
Complaint Warrior: Regulatory Complaint Sender

Streamlit app that:
  1. Reads complaints from a SQLite database, including the Complaint Warrior cw_store.sqlite format.
  2. Uses the OpenAI ChatGPT API to recommend organizations/agencies to contact.
  3. Drafts individualized complaint letters.
  4. Lets the user review/edit recipients and letters.
  5. Sends selected letters through the user's Gmail account using the Gmail API.
  6. Logs all outreach back into SQLite.

Important safety design:
  - The app never sends automatically after GPT generation.
  - The user must check a confirmation box and click Send.
  - BBB/FTC/AG/FDACS are often web-form based. The app prepares text for web forms;
    it sends email only when a verified recipient email is supplied.
  - GPT is instructed not to invent email addresses. Always verify recipient emails.

Run:
  pip install -r requirements_cw_sender.txt
  streamlit run cw_escalation_desk.py

Local-file assumptions:
  - config.ini is in the same directory as this app.
  - config.ini contains [OpenAI] api_key = ...
  - cw_store.sqlite is in the same directory.
  - Gmail files are in the same directory: credentials.json and either
    cw_gmail_tokens.sqlite from gmail_oauth_server.py or gmail_token.json
    generated by this app's local OAuth flow.
"""

