def parse_registry_csv(uploaded) -> Optional[List[Dict[str, str]]]:
    if not uploaded:
        return None
    df = pd.read_csv(uploaded).fillna("")
    required = {"key", "name"}
    if not required.issubset(set(df.columns)):
        raise ValueError("Registry CSV must contain at least columns: key,name")
    rows = []
    for _, row in df.iterrows():
        d = {k: clean_text(row.get(k, "")) for k in df.columns}
        for k in ["category", "recommended_channel", "email", "web_url", "notes"]:
            d.setdefault(k, "")
        rows.append(d)
    return rows


def load_registry_from_config() -> List[Dict[str, str]]:
    """Load an optional organization registry from config.ini.

    Supported:
      [Registry]
      path = organizations.csv

    CSV columns: key,name,category,recommended_channel,email,web_url,notes
    JSON format: a list of objects with the same keys.

    If no file is configured or the file is missing, the built-in registry is used.
    """
    registry_path = local_path(cfg_get("Registry", "path", ""))
    if not registry_path:
        return DEFAULT_ORG_REGISTRY.copy()

    p = Path(registry_path)
    if not p.exists():
        st.warning(f"Registry file configured but not found: {registry_path}. Using default registry.")
        return DEFAULT_ORG_REGISTRY.copy()

    try:
        if p.suffix.lower() == ".json":
            data = json.loads(p.read_text(encoding="utf-8"))
            if not isinstance(data, list):
                raise ValueError("Registry JSON must be a list of objects.")
            rows = []
            for item in data:
                if not isinstance(item, dict):
                    continue
                d = {str(k): clean_text(v) for k, v in item.items()}
                if not d.get("key") or not d.get("name"):
                    continue
                for k in ["category", "recommended_channel", "email", "web_url", "notes"]:
                    d.setdefault(k, "")
                rows.append(d)
            return rows or DEFAULT_ORG_REGISTRY.copy()

        df = pd.read_csv(p).fillna("")
        required = {"key", "name"}
        if not required.issubset(set(df.columns)):
            raise ValueError("Registry CSV must contain at least columns: key,name")
        rows = []
        for _, row in df.iterrows():
            d = {k: clean_text(row.get(k, "")) for k in df.columns}
            for k in ["category", "recommended_channel", "email", "web_url", "notes"]:
                d.setdefault(k, "")
            rows.append(d)
        return rows or DEFAULT_ORG_REGISTRY.copy()
    except Exception as e:
        st.warning(f"Could not load organization registry from {registry_path}: {e}. Using default registry.")
        return DEFAULT_ORG_REGISTRY.copy()


def parse_config_paths(value: str) -> List[str]:
    """Parse comma/newline/semicolon-separated paths from config.ini."""
    value = clean_text(value)
    if not value:
        return []
    parts = re.split(r"[\n;,]+", value)
    return [local_path(x.strip()) for x in parts if x.strip()]


def load_attachment_files_for_record(record: ComplaintRecord) -> List[Tuple[str, bytes]]:
    """Load evidence attachments automatically from the complaint JSON and config.ini.

    Sources:
      - record.docs from Complaint Warrior complaint_json
      - [Attachments] paths = file1.pdf, file2.png
      - [Attachments] directory = cw_uploads

    Missing files are skipped with a visible warning.
    """
    candidates: List[str] = []

    for p in record.docs or []:
        if p:
            candidates.append(local_path(str(p)))

    candidates.extend(parse_config_paths(cfg_get("Attachments", "paths", "")))

    directory = local_path(cfg_get("Attachments", "directory", ""))
    if directory and Path(directory).exists():
        for child in sorted(Path(directory).iterdir()):
            if child.is_file():
                candidates.append(str(child))

    seen = set()
    attachments: List[Tuple[str, bytes]] = []
    missing: List[str] = []
    for item in candidates:
        if not item:
            continue
        p = Path(item)
        key = str(p.resolve()) if p.exists() else str(p)
        if key in seen:
            continue
        seen.add(key)
        if not p.exists() or not p.is_file():
            missing.append(str(p))
            continue
        try:
            attachments.append((p.name, p.read_bytes()))
        except Exception as e:
            st.warning(f"Could not read attachment {p}: {e}")

    if missing:
        with st.expander("Some configured evidence files were not found", expanded=False):
            for m in missing:
                st.write(m)

    return attachments


def auto_load_complaints_once() -> None:
    """Load complaints from config/database path with no user path input."""
    db_path = DEFAULT_DB_PATH
    already_loaded = bool(st.session_state.get("records")) and st.session_state.get("loaded_db_path") == db_path
    attempted = st.session_state.get("auto_load_attempted_for") == db_path
    if already_loaded or attempted:
        return

    st.session_state.auto_load_attempted_for = db_path
    try:
        records, source, mapping = load_complaints(db_path)
        st.session_state.records = records
        st.session_state.loaded_db_path = db_path
        st.session_state.db_source = source
        st.session_state.generic_mapping = mapping
        st.session_state.registry = load_registry_from_config()
        st.session_state.drafts = []
    except Exception as e:
        st.session_state.auto_load_error = str(e)


