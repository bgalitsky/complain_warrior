def open_db(db_path: str) -> sqlite3.Connection:
    if not db_path:
        raise ValueError("Database path is required.")
    if not os.path.exists(db_path):
        raise FileNotFoundError(f"Database not found: {db_path}")
    con = sqlite3.connect(db_path, check_same_thread=False)
    con.row_factory = sqlite3.Row
    return con


def table_columns(con: sqlite3.Connection, table: str) -> List[str]:
    return [r[1] for r in con.execute(f"PRAGMA table_info('{table}')").fetchall()]


def list_tables(con: sqlite3.Connection) -> List[str]:
    rows = con.execute(
        "SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
    ).fetchall()
    return [r[0] for r in rows]


def json_loads_safe(s: Any) -> Dict[str, Any]:
    if isinstance(s, dict):
        return s
    if not s:
        return {}
    try:
        x = json.loads(s)
        return x if isinstance(x, dict) else {}
    except Exception:
        return {}


def clean_text(x: Any) -> str:
    if x is None:
        return ""
    text = str(x).replace("\r\n", "\n").replace("\r", "\n")
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def first_existing_key(d: Dict[str, Any], keys: List[str]) -> str:
    for k in keys:
        v = clean_text(d.get(k))
        if v:
            return v
    return ""


def infer_company_name(text: str, subject: str = "") -> str:
    # Lightweight inference only. The user can always override.
    combined = f"{subject}\n{text}"
    patterns = [
        r"(?:against|from|with|by)\s+([A-Z][A-Za-z0-9&.'\- ]{2,80})(?:\s+for|\s+because|\s+regarding|[,.;\n]|$)",
        r"company\s+(?:called|named)\s+([A-Z][A-Za-z0-9&.'\- ]{2,80})(?:[,.;\n]|$)",
        r"merchant\s+(?:called|named)\s+([A-Z][A-Za-z0-9&.'\- ]{2,80})(?:[,.;\n]|$)",
    ]
    for pat in patterns:
        m = re.search(pat, combined, flags=re.IGNORECASE)
        if m:
            candidate = re.sub(r"\s+", " ", m.group(1)).strip(" .,-")
            if 2 <= len(candidate) <= 80:
                return candidate
    return ""


def load_complaint_warrior_native(con: sqlite3.Connection) -> List[ComplaintRecord]:
    """Load the native Complaint Warrior table: complaints(user_email, complaint_id, complaint_json, updated_at)."""
    rows = con.execute(
        "SELECT user_email, complaint_id, complaint_json, updated_at FROM complaints ORDER BY updated_at DESC"
    ).fetchall()
    out: List[ComplaintRecord] = []
    for row in rows:
        obj = json_loads_safe(row["complaint_json"])
        raw = clean_text(obj.get("complaint_raw"))
        prof = clean_text(obj.get("complaint_professional"))
        subject = clean_text(obj.get("subject")) or "Consumer complaint"
        company = first_existing_key(obj, ["company_name", "merchant_name", "business_name"])
        if not company:
            company = infer_company_name(raw or prof, subject)
        out.append(
            ComplaintRecord(
                complaint_id=clean_text(row["complaint_id"]) or clean_text(obj.get("complaint_id")),
                subject=subject,
                complaint_text=raw or prof,
                professional_text=prof,
                company_name=company,
                user_name=clean_text(obj.get("user_name")),
                user_email=clean_text(obj.get("user_email")) or clean_text(row["user_email"]),
                created_at=clean_text(obj.get("created_at")),
                docs=obj.get("docs") or [],
                source_table="complaints",
                raw_json=obj,
            )
        )
    return out


def pick_generic_columns(columns: List[str]) -> Dict[str, Optional[str]]:
    norm = {re.sub(r"[^a-z0-9]", "", c.lower()): c for c in columns}

    def find(names: List[str]) -> Optional[str]:
        for n in names:
            hit = norm.get(re.sub(r"[^a-z0-9]", "", n.lower()))
            if hit:
                return hit
        # fallback substring
        for k, original in norm.items():
            if any(re.sub(r"[^a-z0-9]", "", n.lower()) in k for n in names):
                return original
        return None

    return {
        "id": find(["complaint_id", "case_id", "id", "claim_id"]),
        "subject": find(["subject", "title", "issue", "case_title"]),
        "text": find(["complaint", "complaint_text", "complaint_raw", "description", "issue_summary", "claim_summary"]),
        "professional": find(["complaint_professional", "professional_text", "final_text"]),
        "company": find(["company_name", "merchant", "merchant_name", "business_name", "defendant"]),
        "user_name": find(["user_name", "consumer_name", "customer_name", "plaintiff_name"]),
        "user_email": find(["user_email", "consumer_email", "customer_email", "email"]),
        "created_at": find(["created_at", "created", "date", "updated_at"]),
    }


def load_generic_complaints(con: sqlite3.Connection) -> Tuple[List[ComplaintRecord], str, Dict[str, Optional[str]]]:
    tables = list_tables(con)
    best = None
    best_score = -1
    for t in tables:
        cols = table_columns(con, t)
        mapping = pick_generic_columns(cols)
        score = sum(1 for v in mapping.values() if v)
        if score > best_score and mapping.get("text"):
            best = t
            best_score = score
    if not best:
        return [], "", {}

    mapping = pick_generic_columns(table_columns(con, best))
    rows = con.execute(f"SELECT * FROM '{best}' ORDER BY rowid DESC LIMIT 250").fetchall()
    records: List[ComplaintRecord] = []
    for i, row in enumerate(rows):
        d = dict(row)
        subject = clean_text(d.get(mapping.get("subject") or "")) or "Consumer complaint"
        text = clean_text(d.get(mapping.get("text") or ""))
        prof = clean_text(d.get(mapping.get("professional") or ""))
        company = clean_text(d.get(mapping.get("company") or "")) or infer_company_name(text or prof, subject)
        cid = clean_text(d.get(mapping.get("id") or "")) or f"ROW-{i+1}"
        records.append(
            ComplaintRecord(
                complaint_id=cid,
                subject=subject,
                complaint_text=text or prof,
                professional_text=prof,
                company_name=company,
                user_name=clean_text(d.get(mapping.get("user_name") or "")),
                user_email=clean_text(d.get(mapping.get("user_email") or "")),
                created_at=clean_text(d.get(mapping.get("created_at") or "")),
                docs=[],
                source_table=best,
                raw_json=d,
            )
        )
    return records, best, mapping


def load_complaints(db_path: str) -> Tuple[List[ComplaintRecord], str, Dict[str, Any]]:
    con = open_db(db_path)
    try:
        tables = list_tables(con)
        if "complaints" in tables:
            cols = set(table_columns(con, "complaints"))
            if {"complaint_id", "complaint_json"}.issubset(cols):
                return load_complaint_warrior_native(con), "Complaint Warrior native complaints table", {}
        records, table, mapping = load_generic_complaints(con)
        return records, f"Generic table: {table}" if table else "No complaint table detected", mapping
    finally:
        con.close()


def ensure_outreach_table(con: sqlite3.Connection) -> None:
    con.execute(
        f"""
        CREATE TABLE IF NOT EXISTS {OUTREACH_TABLE} (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            complaint_id TEXT NOT NULL,
            org_key TEXT,
            org_name TEXT NOT NULL,
            recipient_email TEXT,
            channel TEXT NOT NULL,
            subject TEXT,
            body TEXT,
            gmail_message_id TEXT,
            status TEXT NOT NULL,
            error TEXT,
            sent_at REAL NOT NULL,
            created_at_iso TEXT NOT NULL
        )
        """
    )
    con.commit()


def log_outreach(
    db_path: str,
    complaint_id: str,
    draft: OutreachDraft,
    status: str,
    gmail_message_id: str = "",
    error: str = "",
) -> None:
    con = open_db(db_path)
    try:
        ensure_outreach_table(con)
        now = time.time()
        con.execute(
            f"""
            INSERT INTO {OUTREACH_TABLE} (
                complaint_id, org_key, org_name, recipient_email, channel, subject, body,
                gmail_message_id, status, error, sent_at, created_at_iso
            ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                complaint_id,
                draft.org_key,
                draft.org_name,
                draft.email,
                draft.recommended_channel,
                draft.subject,
                draft.body,
                gmail_message_id,
                status,
                error,
                now,
                datetime.fromtimestamp(now).isoformat(timespec="seconds"),
            ),
        )
        con.commit()
    finally:
        con.close()


