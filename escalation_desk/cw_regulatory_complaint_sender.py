def is_invalid_grant_error(exc: BaseException) -> bool:
    """True when Google rejected the refresh token and re-auth is required."""
    text = f"{type(exc).__name__}: {exc}".lower()
    return "invalid_grant" in text or "token has been expired or revoked" in text


def _safe_unlink(path: str) -> None:
    if path:
        try:
            Path(path).unlink(missing_ok=True)
        except Exception:
            pass


def _load_token_from_sqlite(token_db_path: str, token_key: str) -> Tuple[Optional[str], Optional[Dict[str, Any]]]:
    """Read a Gmail OAuth token stored by gmail_oauth_server.py / GmailTokenStore."""
    if not token_db_path or not os.path.exists(token_db_path):
        return None, None
    con = sqlite3.connect(token_db_path)
    try:
        rows = con.execute("SELECT key, token_json FROM gmail_tokens ORDER BY updated_at DESC").fetchall()
    except Exception:
        return None, None
    finally:
        con.close()
    if not rows:
        return None, None

    # Prefer configured key. If it does not exist and there is only one account, use that account.
    for key, token_json in rows:
        if key == token_key:
            return key, json_loads_safe(token_json)
    if len(rows) == 1:
        return rows[0][0], json_loads_safe(rows[0][1])
    return None, None


def _delete_sqlite_token(token_db_path: str, token_key: str) -> None:
    if not token_db_path or not os.path.exists(token_db_path) or not token_key:
        return
    try:
        con = sqlite3.connect(token_db_path)
        con.execute("DELETE FROM gmail_tokens WHERE key=?", (token_key,))
        con.commit()
        con.close()
    except Exception:
        pass


def _save_token_to_sqlite(token_db_path: str, token_key: str, token_json: Dict[str, Any]) -> None:
    con = sqlite3.connect(token_db_path)
    con.execute(
        """
        CREATE TABLE IF NOT EXISTS gmail_tokens(
          key TEXT PRIMARY KEY,
          token_json TEXT NOT NULL,
          updated_at REAL NOT NULL
        )
        """
    )
    con.execute(
        "INSERT OR REPLACE INTO gmail_tokens(key, token_json, updated_at) VALUES(?,?,?)",
        (token_key, json.dumps(token_json), time.time()),
    )
    con.commit()
    con.close()


def list_gmail_token_keys(token_db_path: str = GMAIL_TOKEN_DB) -> List[str]:
    if not token_db_path or not os.path.exists(token_db_path):
        return []
    con = sqlite3.connect(token_db_path)
    try:
        rows = con.execute("SELECT key FROM gmail_tokens ORDER BY updated_at DESC").fetchall()
        return [r[0] for r in rows]
    except Exception:
        return []
    finally:
        con.close()


def _credentials_to_token_json(creds: Any) -> Dict[str, Any]:
    return {
        "token": getattr(creds, "token", None),
        "refresh_token": getattr(creds, "refresh_token", None),
        "token_uri": getattr(creds, "token_uri", None),
        "client_id": getattr(creds, "client_id", None),
        "client_secret": getattr(creds, "client_secret", None),
        "scopes": list(getattr(creds, "scopes", None) or SCOPES),
    }


def _creds_from_token_json(token_json: Dict[str, Any]) -> Any:
    return Credentials(
        token=token_json.get("token"),
        refresh_token=token_json.get("refresh_token"),
        token_uri=token_json.get("token_uri"),
        client_id=token_json.get("client_id"),
        client_secret=token_json.get("client_secret"),
        scopes=token_json.get("scopes") or SCOPES,
    )


def _refresh_or_raise_invalid_grant(creds: Any) -> Any:
    """Refresh proactively so invalid_grant is detected before the first send."""
    if not creds:
        return creds
    if getattr(creds, "refresh_token", None):
        creds.refresh(Request())
    return creds


def _run_local_oauth(client_secrets_path: str, token_path: str, token_db_path: str, token_key: str, token_pickle_path: str) -> Any:
    """Start local browser OAuth and save the fresh token to json, sqlite, and pickle fallbacks."""
    if not os.path.exists(client_secrets_path):
        raise FileNotFoundError(
            f"Gmail OAuth credentials not found: {client_secrets_path}. Put credentials.json in the app directory."
        )
    flow = InstalledAppFlow.from_client_secrets_file(client_secrets_path, SCOPES)
    creds = flow.run_local_server(port=0)

    # Save modern JSON token.
    if token_path:
        Path(token_path).write_text(creds.to_json(), encoding="utf-8")

    # Save SQLite token compatible with your gmail_oauth_server.py / GmailTokenStore pattern.
    token_json = _credentials_to_token_json(creds)
    if token_db_path and token_key:
        _save_token_to_sqlite(token_db_path, token_key, token_json)

    # Save legacy pickle compatible with auth_gmail.py.
    if token_pickle_path:
        with open(token_pickle_path, "wb") as f:
            pickle.dump(creds, f)

    return creds


def build_gmail_service(
    client_secrets_path: str = GMAIL_CLIENT_SECRETS,
    token_path: str = GMAIL_TOKEN_FILE,
    token_db_path: str = GMAIL_TOKEN_DB,
    token_key: str = GMAIL_TOKEN_KEY,
    token_pickle_path: str = GMAIL_TOKEN_PICKLE,
    force_reauth: bool = False,
):
    """Build Gmail API service using local files in the app directory.

    This version handles invalid_grant by deleting the bad saved token and starting OAuth again.

    Preferred order:
      1. cw_gmail_tokens.sqlite created by gmail_oauth_server.py / GmailTokenStore.
      2. gmail_token.json created by this app.
      3. token.pickle created by the simple auth_gmail.py helper.
      4. Fresh local OAuth through credentials.json.
    """
    if Credentials is None or build is None or InstalledAppFlow is None:
        raise RuntimeError(
            "Google API packages are not installed. Run: pip install google-api-python-client google-auth-oauthlib google-auth-httplib2"
        )

    if force_reauth:
        _delete_sqlite_token(token_db_path, token_key)
        _delete_sqlite_token(token_db_path, "default")
        _safe_unlink(token_path)
        _safe_unlink(token_pickle_path)

    # 1) SQLite token from gmail_oauth_server.py.
    selected_key, token_json = _load_token_from_sqlite(token_db_path, token_key)
    if token_json:
        try:
            creds = _creds_from_token_json(token_json)
            # Proactively refresh to catch invalid_grant now, not during send().
            creds = _refresh_or_raise_invalid_grant(creds)
            refreshed = _credentials_to_token_json(creds)
            _save_token_to_sqlite(token_db_path, selected_key or token_key, refreshed)
            if creds and creds.valid:
                return build("gmail", "v1", credentials=creds, cache_discovery=False)
        except Exception as e:
            if is_invalid_grant_error(e):
                _delete_sqlite_token(token_db_path, selected_key or token_key)
            else:
                raise

    # 2) JSON token fallback.
    token_file = Path(token_path)
    if token_file.exists():
        try:
            creds = Credentials.from_authorized_user_file(str(token_file), SCOPES)
            if getattr(creds, "refresh_token", None):
                creds = _refresh_or_raise_invalid_grant(creds)
                token_file.write_text(creds.to_json(), encoding="utf-8")
            if creds and creds.valid:
                return build("gmail", "v1", credentials=creds, cache_discovery=False)
        except Exception as e:
            if is_invalid_grant_error(e):
                _safe_unlink(token_path)
            else:
                raise

    # 3) token.pickle fallback from auth_gmail.py.
    pickle_file = Path(token_pickle_path)
    if pickle_file.exists():
        try:
            with open(pickle_file, "rb") as f:
                creds = pickle.load(f)
            if getattr(creds, "refresh_token", None):
                creds = _refresh_or_raise_invalid_grant(creds)
                with open(pickle_file, "wb") as f:
                    pickle.dump(creds, f)
            if creds and creds.valid:
                return build("gmail", "v1", credentials=creds, cache_discovery=False)
        except Exception as e:
            if is_invalid_grant_error(e):
                _safe_unlink(token_pickle_path)
            else:
                raise

    # 4) Fresh OAuth when all saved tokens are missing/revoked/expired.
    creds = _run_local_oauth(client_secrets_path, token_path, token_db_path, token_key, token_pickle_path)
    return build("gmail", "v1", credentials=creds, cache_discovery=False)


def make_gmail_message(
    to_email: str,
    subject: str,
    body_text: str,
    attachments: Optional[List[Tuple[str, bytes]]] = None,
) -> Dict[str, str]:
    attachments = attachments or []
    msg = MIMEMultipart()
    msg["To"] = to_email
    msg["Subject"] = subject
    msg.attach(MIMEText(body_text, "plain", "utf-8"))

    for filename, data in attachments:
        ctype, encoding = mimetypes.guess_type(filename)
        if ctype is None or encoding is not None:
            ctype = "application/octet-stream"
        maintype, subtype = ctype.split("/", 1)
        part = MIMEBase(maintype, subtype)
        part.set_payload(data)
        encoders.encode_base64(part)
        part.add_header("Content-Disposition", "attachment", filename=os.path.basename(filename))
        msg.attach(part)

    raw = base64.urlsafe_b64encode(msg.as_bytes()).decode("utf-8")
    return {"raw": raw}


def gmail_send(
    service: Any,
    to_email: str,
    subject: str,
    body_text: str,
    attachments: Optional[List[Tuple[str, bytes]]] = None,
) -> Dict[str, Any]:
    body = make_gmail_message(to_email, subject, body_text, attachments)
    return service.users().messages().send(userId="me", body=body).execute()


def gmail_send_with_reauth(
    to_email: str,
    subject: str,
    body_text: str,
    attachments: Optional[List[Tuple[str, bytes]]] = None,
) -> Dict[str, Any]:
    """Send once, and if invalid_grant occurs, force OAuth re-connect and retry once."""
    service = build_gmail_service(
        GMAIL_CLIENT_SECRETS,
        GMAIL_TOKEN_FILE,
        GMAIL_TOKEN_DB,
        GMAIL_TOKEN_KEY,
        GMAIL_TOKEN_PICKLE,
        force_reauth=False,
    )
    try:
        return gmail_send(service, to_email, subject, body_text, attachments=attachments)
    except Exception as e:
        if not is_invalid_grant_error(e):
            raise
        # Saved token was revoked/expired. Delete bad tokens, re-authenticate, retry exactly once.
        service = build_gmail_service(
            GMAIL_CLIENT_SECRETS,
            GMAIL_TOKEN_FILE,
            GMAIL_TOKEN_DB,
            GMAIL_TOKEN_KEY,
            GMAIL_TOKEN_PICKLE,
            force_reauth=True,
        )
        return gmail_send(service, to_email, subject, body_text, attachments=attachments)


